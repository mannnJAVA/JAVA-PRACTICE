
import java.util.Scanner;

public class revers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Print Reverse natural number \n Enter the value of n : ");
        int n =sc.nextInt();
        for (int i = n; i >=0;--i) {
            System.out.println(i+"");
            
        }
    }
    
}
